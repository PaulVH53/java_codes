// Demonstrate casts.
class Promotion {
	public static void main(String[] args) {
		byte a = 40;
		byte b = 50;
		byte c = 100;
		
		int d = a * b / c;
		
		System.out.println("byte a = 40");
		System.out.println("byte b = 50");
		System.out.println("byte c = 100");
		System.out.println("\nint d = a * b / c = " + d);
		
	}
}